package ex_08_Increment_Decrement_Operator;

public class Lab075_Post_Increment {
    public static void main(String[] args) {

        // POST increment -> Print the value first and then increase the value
        // and assign it to the variable

        int a = 10;
        System.out.println(a++); // a=>10
        System.out.println(a); //a -> 10+1 ->11
    }
}
