package ex_10_Switch_Case;

public class Lab091_Interview_Ex1 {
    public static void main(String[] args) {
        int a = 10;
        switch (a) {
            case 10 :
                System.out.println("Hello");
                break;

            case 20 :
                System.out.println("Hi");
                break;

            default :
                System.out.println("Bye");
                break;
        }

        //Int is allowed in switch case.
    }
}
