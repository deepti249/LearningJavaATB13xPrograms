package ex_10_Switch_Case;

public class Lab099_Interview_Ex2 {
    public static void main(String[] args) {
        char code = 'C';

        switch (code){
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
