package ex_12_While_Loop;

public class Lab121_While_Loop_Interview_Ex1 {
    public static void main(String[] args) {
        int i1 = 10;
        while (i1>=1){  //10 >=1 so it will run from 10 to 1 i.e. 10 times in reverse order
            System.out.println(i1);
            i1--;
        }
    }
}
